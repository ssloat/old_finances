from sqlalchemy import Column, Integer, String, ForeignKey, Boolean
from sqlalchemy.orm import relationship, backref

from finances import Base

class Category(Base):
    __tablename__ = 'categories'

    id        = Column(Integer, primary_key=True)
    parent_id = Column(Integer, ForeignKey('categories.id'))
    name      = Column(String)
    depth     = Column(Integer, default=0)

    children = relationship("Category", 
                backref=backref('parent', remote_side=[id])
            )

    def __init__(self, name, parent=None, depth=0):
        self.name   = name
        if parent:
            self.parent = parent
            self.depth  = parent.depth + 1
        else:
            self.depth = depth

    def __repr__(self):
       return "<Category('%s')>" % (self.name)

  

class CategoryRE(Base):
    __tablename__ = 'category_res'

    id          = Column(Integer, primary_key=True)
    name        = Column(String)
    category_id = Column(Integer, ForeignKey('categories.id'))
    pattern     = Column(String)
    yearly      = Column(Boolean, default=False)

    category = relationship("Category")

    def __init__(self, name, category, pattern, yearly=False):
        self.name = name
        self.category = category
        self.pattern = pattern
        self.yearly = yearly

    def __repr__(self):
       return "<CategoryRE('%s, %s, %s')>" % (self.name, self.category.name,
               self.pattern)


